    var seat_anzahl = 10+1,
        seat_ah = seat_anzahl/2,
        row_anzahl = 10+1;

    $('#reservation .seat').click(function(){
        var seat_nr = $(this).index()+1,
            seat = $(this),
            row = seat.parent(),
            row_nr = row.index()+1,
            realseat = $('#cinema .row:nth-child('+row_nr+')').find('.seat:nth-child('+seat_nr+')'),
            translateX = (78.5+(40.5*(row_anzahl-row_nr)))*((seat_ah-(seat_nr-0.5))/seat_ah),
            translateY = 8*row_nr+22,
            rotateY = Math.ceil((seat_ah-seat_nr)/seat_ah*21)+1,
            rotateX = (row_anzahl-row_nr)*0.5,
            translateZ = 120*(row_anzahl-row_nr)+1000;

        if(rotateY < 0) {
            translateZ = translateZ+(100-rotateY*-5);
        } else {
            translateZ = translateZ+(100-rotateY*5);
        }

        var transform = 'translateX(-50%) translate3d('+translateX+'px,'+translateY+'px,'+translateZ+'px) rotateY('+rotateY+'deg) rotateX('+rotateX+'deg) scale3d(1,1,2)';

        $('#cinema').css({
            '-webkit-transform': transform,
            '-moz-transform': transform,
            'transform': transform,
        });

    });
